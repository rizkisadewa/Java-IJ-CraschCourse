package com.company;

public class Main {

    public static void main(String[] args) {
	// write your code here
        // for ascending
        System.out.println("For Ascending");
        for (int i = 0; i <= 5; i++){
            System.out.println(i);
        }

        // for descending
        System.out.println("For Descending");
        for (int i = 5; i >= 0; i--){
            System.out.println(i);
        }
    }
}
